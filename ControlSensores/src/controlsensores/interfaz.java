     void guardarJson(){
        
          try(Writer writer =  new OutputStreamWriter(new FileOutputStream(getClass().getResource("sensores.JSON").toURI().getPath()),"UTF-8"))
        {        
           Gson gson = new GsonBuilder().setPrettyPrinting().create();
           gson.toJson(_sensores,writer);
           writer.close();
        } catch (Exception e) {
            e.printStackTrace();
        }

      
     }
